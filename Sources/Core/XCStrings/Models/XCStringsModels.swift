import Foundation

// MARK: - Core Data Structures

/// Root structure of xcstrings file
public struct XCStringsFile: Codable, Sendable {
    public var sourceLanguage: String
    public var strings: [String: StringEntry]
    public var version: String

    public init(
        sourceLanguage: String = "en",
        strings: [String: StringEntry] = [:],
        version: String = "1.0",
    ) {
        self.sourceLanguage = sourceLanguage
        self.strings = strings
        self.version = version
    }
}

/// String entry for each key
public struct StringEntry: Codable, Sendable {
    public var comment: String?
    public var isCommentAutoGenerated: Bool?
    public var extractionState: String?
    public var shouldTranslate: Bool?
    public var localizations: [String: Localization]?

    /// `false` only when the entry is explicitly marked non-translatable in Xcode (Localization
    /// Properties → "Should Translate" off).
    public var requiresTranslation: Bool { shouldTranslate != false }

    public init(
        comment: String? = nil,
        isCommentAutoGenerated: Bool? = nil,
        extractionState: String? = nil,
        shouldTranslate: Bool? = nil,
        localizations: [String: Localization]? = nil,
    ) {
        self.comment = comment
        self.isCommentAutoGenerated = isCommentAutoGenerated
        self.extractionState = extractionState
        self.shouldTranslate = shouldTranslate
        self.localizations = localizations
    }
}

/// Localization entry
public struct Localization: Codable, Sendable {
    public var stringUnit: StringUnit?
    public var variations: Variations?

    public init(stringUnit: StringUnit? = nil, variations: Variations? = nil) {
        self.stringUnit = stringUnit
        self.variations = variations
    }
}

/// String unit containing the actual translation value
public struct StringUnit: Codable, Sendable {
    public var state: String
    public var value: String

    public init(state: String = "translated", value: String) {
        self.state = state
        self.value = value
    }
}

/// Variations (plural, device, etc.)
public struct Variations: Codable, Sendable {
    public var plural: PluralVariation?
    public var device: DeviceVariation?

    public init(plural: PluralVariation? = nil, device: DeviceVariation? = nil) {
        self.plural = plural
        self.device = device
    }
}

/// Wrapper for a variation category value.
///
/// In xcstrings JSON, each variation value is
/// `{ "stringUnit": { "state": "...", "value": "..." } }`.
public struct VariationValue: Codable, Sendable {
    public var stringUnit: StringUnit?

    public init(stringUnit: StringUnit? = nil) { self.stringUnit = stringUnit }
}

/// Plural variation
public struct PluralVariation: Codable, Sendable {
    public var zero: VariationValue?
    public var one: VariationValue?
    public var two: VariationValue?
    public var few: VariationValue?
    public var many: VariationValue?
    public var other: VariationValue?

    public init(
        zero: VariationValue? = nil,
        one: VariationValue? = nil,
        two: VariationValue? = nil,
        few: VariationValue? = nil,
        many: VariationValue? = nil,
        other: VariationValue? = nil,
    ) {
        self.zero = zero
        self.one = one
        self.two = two
        self.few = few
        self.many = many
        self.other = other
    }
}

/// Device variation
public struct DeviceVariation: Codable, Sendable {
    public var iphone: VariationValue?
    public var ipad: VariationValue?
    public var mac: VariationValue?
    public var applewatch: VariationValue?
    public var appletv: VariationValue?

    public init(
        iphone: VariationValue? = nil,
        ipad: VariationValue? = nil,
        mac: VariationValue? = nil,
        applewatch: VariationValue? = nil,
        appletv: VariationValue? = nil,
    ) {
        self.iphone = iphone
        self.ipad = ipad
        self.mac = mac
        self.applewatch = applewatch
        self.appletv = appletv
    }
}

